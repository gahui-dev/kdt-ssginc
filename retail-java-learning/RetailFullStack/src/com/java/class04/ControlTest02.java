package com.java.class04;

public class ControlTest02 {

	public static void main(String[] args) {
		// for문
		/*
		 * for(초기식; 조건식; 증감식) { 반복 실행문(들) }
		 */
		for(int i = 0; i< 10; i++) {
			System.out.println("for "+ i);
		}

		int i = 0;
		while(i < 10){
			System.out.println("while "+ i);
			i++;
		}

	}

}
